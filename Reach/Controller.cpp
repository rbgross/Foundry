/***************************************************************************************************
* update function
*
* Placeholder.
***************************************************************************************************/
void Controller::update( void )
{	
	if ( Global::window->getKey( 'W' ) ) {
		this->transform->translate( glm::vec3( 0.0f, 0.0f, 0.1f ) );
	}

	if ( Global::window->getKey( 'S' ) ) {
		this->transform->translate( glm::vec3( 0.0f, 0.0f, -0.1f ) ); 
	}

	if ( Global::window->getKey( 'A' ) ) {
		this->transform->rotate( 2.0f, glm::vec3( 0.0f, 1.0f, 0.0f ) ); 
	}

	if ( Global::window->getKey( 'D' ) ) {
		this->transform->rotate( -2.0f, glm::vec3( 0.0f, 1.0f, 0.0f ) ); 
	}
}